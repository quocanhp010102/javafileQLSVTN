    public void saveNumberToFile(int number, String fileName) {
        try (DataOutputStream dos = new DataOutputStream(new FileOutputStream(fileName))) {
            dos.writeInt(number);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public int readNumberFromFile(String fileName) {
        int number = 0;
        try (DataInputStream dis = new DataInputStream(new FileInputStream(fileName))) {
            number = dis.readInt();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return number;
    }
