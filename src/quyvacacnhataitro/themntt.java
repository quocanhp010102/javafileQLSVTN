        jTextField2.getText();
        jTextField3.getText();
        if (jTextField1.getText() == "" || jTextField2.getText() == "" || jTextField3.getText() == "") {
            JOptionPane.showMessageDialog(rootPane, "khong duoc de trong o nao");
        } else {
            boolean isOk = true;

            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            dateFormat.setLenient(false); // Chặn việc chuyển đổi chuỗi không hợp lệ
            try {
                Date date = dateFormat.parse(jTextField3.getText());
                System.out.println("Ngày tháng hợp lệ: " + date);
            } catch (Exception e) {
                isOk = false;
                JOptionPane.showMessageDialog(rootPane, "Ngay tháng phải đẻ dạng YYYY-MM-DD!");
                System.out.println("Ngày tháng không hợp lệ: " + jTextField3.getText());
            }
            if (isOk) {
                soNTT = readNumberFromFile(fileNameSoNTT);
                nhataitro b = new nhataitro();
                b.setMaNTT(soNTT);
                b.setTenNTT(jTextField1.getText());
                b.setNgayTaiTro(jTextField3.getText());
                b.setSoTien(Integer.parseInt(jTextField2.getText()));
                soNTT++;
                saveNumberToFile(soNTT, fileNameSoNTT);
                System.out.println(b);
                dsntt.AddThanhVien(b);
                JOptionPane.showMessageDialog(rootPane, "them thanh cong!!");
            }
        }


